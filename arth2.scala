object Operators2 {
    def main(args: Array[String]): Unit={
        var a = 50;
        var b = 30;
        println("Equality of a == b is: "+(a==b));
        println("Inequality of a != b is: "+(a!=b));
        println("Greatest of a > b is: "+(a>b));
        println("Least of a < b is: "+(a<b));
        println("Greater than or equal to of a >= b is: "+(a>=b));
        println("Lesser than or equal to of a <= b is: "+(a<=b));
    }
}